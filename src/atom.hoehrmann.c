void
atom_hoehrmann_spin(
    unsigned char const *begin,
    unsigned char const *end,
    uint64_t *num_codepoints,
    uint64_t *num_errors )
{
    uint32_t state = 0;
    uint32_t codepoint;
    uint64_t nerrors = 0;
    uint64_t ncodepoints = 0;
    unsigned char const *p = begin;
    for (; p < end; ++p) {
        if (bh_utf8_decode(&state, &codepoint, *p) == UTF8_REJECT) {
            nerrors += 1;
        }
        else {
            ncodepoints += 1;
        }
    }
    *num_errors = nerrors;
    *num_codepoints += ncodepoints;
}
